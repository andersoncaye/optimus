    private Object pesquisar(IDAO_T t) {
        IjdPesquisaSimples pes = new IjdPesquisaSimples(new javax.swing.JFrame(), true, t);
        pes.setLocationRelativeTo(this);
        pes.setVisible(true);

        return pes.getResultado();
    }

    private void preencher() {
        txCodigo.setText(produto.getCodigo());
        txDescricao.setText(produto.getDescricao());
        txUnidade.setText(produto.getUnidade());
    }

    private void limpar() {
        produto = new ProdutoMaterial();
        txCodigo.setText("");
        txDescricao.setText("");
        txUnidade.setText("");
    }
