    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        String r = new ProdutoMaterialDao().excluir(produto.getIdproduto());
        if (r == null) {
            JOptionPane.showMessageDialog(null, GemaMsg.deletado("Produto"));
            limpar();
            btnCancelar.setEnabled(false);
            btnDeletar.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, GemaMsg.erroDeletado("Produto") + "\n\n" + r);
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txCodigo.getText().trim().isEmpty()
                && !txDescricao.getText().trim().isEmpty()
                && !txUnidade.getText().trim().isEmpty()) {
            produto.setCodigo(txCodigo.getText());
            produto.setDescricao(txDescricao.getText());
            produto.setUnidade(txUnidade.getText());
            
            String r = new ProdutoMaterialDao().salvar(produto);
            
            if (r == null) {
                JOptionPane.showMessageDialog(null, GemaMsg.salvo("Produto"));
                limpar();
            } else {
                JOptionPane.showMessageDialog(null, GemaMsg.erroSalvar("Produto") + "\n\n" + r);
            }

        } else {
            JOptionPane.showMessageDialog(null, GemaMsg.campoVazio());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


