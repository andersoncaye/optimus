    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        Usuario us = usuario;
        if (TxSenha.getText().trim().isEmpty()) {
            if (TxNome.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha o campo nome!");
            } else {
                us.setNome(TxNome.getText());
                String r = usuarioDao.atualizar(us);

                if (r == null) {
                    JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso!");
                    usuario = us;
                    enabSalvar();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro a atualizar dados do usuário. \n" + r);
                }
            }
        } else {
            try {
                us.setPass(Gema.criptoSenha(TxSenha.getText()));
            } catch (Exception ex) {
                Logger.getLogger(IjfUsuarioLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (TxNome.getText().trim().isEmpty()) {

                String r = usuarioDao.atualizar(us);

                if (r == null) {
                    JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso!");
                    usuario = us;
                    enabSalvar();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro a atualizar dados do usuário. \n" + r);
                }

            } else {
                us.setNome(TxNome.getText());
                String r = usuarioDao.atualizar(us);

                if (r == null) {
                    JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso!");
                    usuario = us;
                    enabSalvar();
                } else {
                    JOptionPane.showMessageDialog(null, "Erro a atualizar dados do usuário. \n" + r);
                }

            }
        }
        
        
        atu(usuario);
        
    }//GEN-LAST:event_BtnSalvarActionPerformed


