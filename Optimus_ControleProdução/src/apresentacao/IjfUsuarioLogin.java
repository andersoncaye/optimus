    private void atu (Usuario u){
        TxId.setText(u.getIdusuario() + "");
        TxUser.setText(u.getNick());
        TxNome.setText(u.getNome());
        String status="Inativo";
        if(u.isAtivo()){
            status = "Ativo";
        }
        txStatus.setText(status);
    }
    
    private void enabSalvar (){
        BtnDesativar.setEnabled(true);
        BtnEditar.setEnabled(true);
        BtnSalvar.setEnabled(false);
        
        TxNome.setEnabled(false);
        TxSenha.setEnabled(false);
    }
