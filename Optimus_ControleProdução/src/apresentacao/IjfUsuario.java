    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        if (Gema.vazio(JtfNome.getText(), 1)
                && Gema.vazio(JtfNick.getText(), 1)
                && Gema.vazio(JtxSenha.getText(), 0)) {

            Usuario u = new Usuario();
            u.setIdusuario(id);
            u.setNome(JtfNome.getText());
            u.setNick(JtfNick.getText());

            if (CboxSituacao.getSelectedIndex() == 0) { //se 0 então true
                u.setAtivo(true);
            } else { //se 1 então false
                u.setAtivo(false);
            }

            UsuarioDao udao = new UsuarioDao();
            String s = "";
            if (id == -1) {

                try {
                    u.setPass(Gema.criptoSenha(JtxSenha.getText()));
                } catch (Exception ex) {
                    Logger.getLogger(IjfUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }

                s = udao.salvar(u);
            } else {
                
                if(!new UsuarioDao().consultarId(id).getPass().equals(JtxSenha.getText())){
                    try {
                        u.setPass(Gema.criptoSenha(JtxSenha.getText()));
                    } catch (Exception ex) {
                        Logger.getLogger(IjfUsuario.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    u.setPass(JtxSenha.getText());
                }
                
                s = udao.atualizar(u);
            }

            if (s == null) {
                JOptionPane.showMessageDialog(null, "Usuario salvo com sucesso.");
                JtfNome.setText("");
                JtfNick.setText("");
                JtxSenha.setText("");
                id = -1;
                JtfNome.requestFocus();
                new UsuarioDao().popularTabela(TbUsuarios, JtxPesquisar.getText(), this.idUsuario);
            } else {
                JOptionPane.showMessageDialog(null, "Problemas ao salvar.\n"
                        + "...\n"
                        + s);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.\n");
        }
    }//GEN-LAST:event_BtnSalvarActionPerformed

