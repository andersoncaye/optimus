    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        int linhaSelecionada = TbUsuarios.getSelectedRow();
        id = (int) TbUsuarios.getValueAt(linhaSelecionada, 0);

        JtxSenha.setEnabled(false);

        UsuarioDao usdao = new UsuarioDao();
        Usuario us = usdao.consultarId(id);

        if (us != null) {
            JtfNome.setText(us.getNome());
            JtfNick.setText(us.getNick());
            JtxSenha.setText(us.getPass());

            int situacaoIndex;
            if ("Ativo".equals((String) TbUsuarios.getValueAt(linhaSelecionada, 3))) {
                situacaoIndex = 0;
            } else {
                situacaoIndex = 1;
            }

            CboxSituacao.setSelectedIndex(situacaoIndex);
        } else {

        }

    }//GEN-LAST:event_BtnEditarActionPerformed

    private void BtnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeletarActionPerformed
        int linhaSelecionada = TbUsuarios.getSelectedRow();
        id = (int) TbUsuarios.getValueAt(linhaSelecionada, 0);

        UsuarioDao usdao = new UsuarioDao();
        String s = usdao.excluir(id);
        if (s == null) {
            JOptionPane.showMessageDialog(null, "Usuario deletado com sucesso.");
            JtfNome.requestFocus();
            new UsuarioDao().popularTabela(TbUsuarios, JtxPesquisar.getText(), this.idUsuario);
        } else {
            JOptionPane.showMessageDialog(null, "Problemas ao deletar.\n"
                    + "...\n"
                    + s);
        }
    }//GEN-LAST:event_BtnDeletarActionPerformed


